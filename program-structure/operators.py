# arithmetic operators

x = 4
y = 3
 
x + y  # returns 7
x - y  # returns 1
x * y  # returns 12
x ** y # returns 64
x / y  # returns 1.333
x // y # returns 1
x % y # returns 1

#assignment operators

x = 4	#Assign 4 to x
x += 4	#Add 4 to existing value of x
x -= 4	#Subtract 4 from existing value of x
x *= 4	#Multiply existing value by 4
x /= 4	#Divide existing value by 4
x %= 4	#Modulo existing value by 4

# comparison operators
 
x == y # returns False
x != y # returns True
x > y  # returns True
x < y  # returns False
x >= y # returns True
x <= y # returns False

# logical operators
x = 4
y = 3
 
x > 2 and y > 1      # returns True
x > 5 or y <= 3      # returns True
not(x > 2 and y > 1) # returns False    this is basically ! in JS